    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            "title" => "required",
        ]);
        if ($request->password==='bookgroup'){
            $group=new Group();
            $group->title=$request->title;
            $group->save();
            return redirect()->route("group.create")->with("toast","Group Add Successful");
        }else{
            return redirect()->route("group.create")->with("toast","Incorrect Password");
        }


    }

